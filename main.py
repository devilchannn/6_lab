def pr3():
    import random

    sp1 = ['Чел1', 'Чел2', 'Чел3', 'Чел4', 'Чел5']
    alph = {'Китайский': "", 'Русский': "", 'Английский': "", 'Японский': "", 'Немецкий': ""}
    sp3 = []

    for i in alph:
        q = random.randint(1, 3)
        alph[i] = random.choice(str(q))
        for j in range(q):
            sp3.append(random.choice(sp1))
    print(sp3)
    for i in range(len(sp3) - 1):
        while sp3[i] == sp3[i + 1]:
            sp3[i + 1] = random.choice(sp1)
    print(sp3)

    print(alph)
    print(sp3[:int(alph['Китайский'])])

